import random
import langchain_openai
import langchain_core
def generate_questions() -> list[str]:
    questions = ['Why should files end with a blank line?']
    return random.sample(questions, min(len(questions), 5))


def generate_answers(question: str, answer: str, _: str) -> bool | str:
    reference_answers = dict(zip(['Why should files end with a blank line?'], ['Files should end with a blank line as per Unix-like convention for text files. Ending a file with exactly one new line character helps in maintaining consistency and avoiding errors like w292 or w391 in tools like flake8. It also ensures proper formatting and readability of the file.']))
    chat = langchain_openai.ChatOpenAI(model="gpt-3.5-turbo-1106", temperature=0)
    chat_answer = chat.invoke(
        [
            langchain_core.messages.SystemMessage(content="You will be given question, reference answer pair and users answer. "
                                  'You have to decide if the answer is correct. '
                                  'If it is respond with a single work "Correct" otherwise return a hint about the answer. '),
            langchain_core.messages.SystemMessage(content='The question: ' + question + '\n'
                                  'The reference answer: ' + reference_answers[question]),
            langchain_core.messages.HumanMessage(
                content=answer
            )
        ]
    ).content
    if chat_answer.startswith("Correct"):
        return True
    return chat_answer
